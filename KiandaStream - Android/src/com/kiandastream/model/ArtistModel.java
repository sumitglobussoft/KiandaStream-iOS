package com.kiandastream.model;

public class ArtistModel 
{
	private String artist_id;
	private String artist_name;
	private String artist_totalsong;
	public String getArtist_id() {
		return artist_id;
	}
	public void setArtist_id(String artist_id) {
		this.artist_id = artist_id;
	}
	public String getArtist_name() {
		return artist_name;
	}
	public void setArtist_name(String artist_name) {
		this.artist_name = artist_name;
	}
	public String getArtist_totalsong() {
		return artist_totalsong;
	}
	public void setArtist_totalsong(String artist_totalsong) {
		this.artist_totalsong = artist_totalsong;
	}
	
}
